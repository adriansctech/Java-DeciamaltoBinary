    //This method check the number if positive or negative
    private boolean checkNumberPositive(int numberToCheck){
        if(numberToCheck>0){
            pass = true;
        }else{
            pass = false;
        }
        return pass;
    }
    
    private boolean checkNumberOrLetter(String stringToCheck){
        try{
            if(stringToCheck.matches(".*\\d.*")){
                pass = true;
            } else{
                pass = false;
            }
        }catch (NumberFormatException e){
            
        }
        return pass;
    }   
    
    
    
