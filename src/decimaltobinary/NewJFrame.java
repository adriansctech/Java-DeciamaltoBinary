    private void convertJbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertJbuttonActionPerformed
        if(numberDecimalJtextArea.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Please insert a decimal number");
        }else{
            if(checkNumberOrLetter(numberDecimalJtextArea.getText())){
                numberDecimal = Integer.parseInt(numberDecimalJtextArea.getText());
                if(checkNumberPositive(numberDecimal)){
                    numberBinaryJtextArea.setText(Integer.toString(numberDecimal,2));
                }else{
                    JOptionPane.showMessageDialog(null, "Only positive numbers");
                    clearJtextAreas();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Only numbers");
                clearJtextAreas();
            }
            convertJbutton.setEnabled(false);
            clearJbutton.setFocusable(true);
        }
    }//GEN-LAST:event_convertJbuttonActionPerformed

    private void clearJbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearJbuttonActionPerformed
        clearJtextAreas();
        convertJbutton.setEnabled(true);
    }//GEN-LAST:event_clearJbuttonActionPerformed

    private void clearJtextAreas(){
        numberBinaryJtextArea.setText("");
        numberDecimalJtextArea.setText("");
    }
    
    
    
